def plot_top10_by_metric(df, metric):

    # -------------------------------
    # Prepare Data
    # -------------------------------
    data = df[['name', 'type', metric]].dropna().copy()
    data['type'] = data['type'].str.strip().str.upper()

    # -------------------------------
    # Top 10 Movies & Shows
    # -------------------------------
    top_movies = data[data['type'] == 'MOVIE'].nlargest(10, metric)
    top_shows  = data[data['type'] == 'SHOW'].nlargest(10, metric)

    top_combined = pd.concat([top_movies, top_shows])

    # -------------------------------
    # Sort for correct ranking
    # -------------------------------
    top_combined = top_combined.sort_values(by=metric, ascending=True)

    # -------------------------------
    # New Custom Colors
    # -------------------------------
    color_map = {
        'MOVIE': '#2EC4B6',   # Teal
        'SHOW': '#9D4EDD'     # Purple
    }

    # -------------------------------
    # Plot
    # -------------------------------
    fig = px.bar(
        top_combined,
        x=metric,
        y='name',
        color='type',
        color_discrete_map=color_map,
        orientation='h',
        title=f"Top 10 Movies & Shows by {metric}"
    )

    # Keep order
    fig.update_yaxes(
        categoryorder='array',
        categoryarray=top_combined['name']
    )

    # Styling
    fig.update_layout(
        template='plotly_dark',
        paper_bgcolor='#0F171E',
        plot_bgcolor='#0F171E',
        font=dict(color='white'),
        title_x=0.5,
        height=600
    )

    fig.show()

"""#### `Top 10 Movies and TV Shows by AMprime Score`"""

plot_top10_by_metric(Amazon_Prime, 'AMprime_Score')

"""#### `Top 10 Movies and TV Shows by AMprime Popularity`"""

plot_top10_by_metric(Amazon_Prime, 'AMprime_Popularity')

"""#### `Top 10 Movies and TV Shows by AMprime Rating`"""

plot_top10_by_metric(Amazon_Prime, 'AMprime_Rating')