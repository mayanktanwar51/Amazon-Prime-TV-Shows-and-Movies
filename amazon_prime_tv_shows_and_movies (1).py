def overall_outlier_flag(df, group_col, cols):

    df = df.copy()
    outlier_flag = pd.Series(False, index=df.index)

    for col in cols:

        # Step 1: Calculate bounds per group
        bounds = df.groupby(group_col)[col].agg(
            Q1=lambda x: x.quantile(0.25),
            Q3=lambda x: x.quantile(0.75)
        )

        bounds['IQR'] = bounds['Q3'] - bounds['Q1']
        bounds['LowerBound'] = bounds['Q1'] - 1.5 * bounds['IQR']
        bounds['UpperBound'] = bounds['Q3'] + 1.5 * bounds['IQR']

        # Step 2: Map bounds to rows
        temp = df[[group_col, col]].merge(
            bounds[['LowerBound','UpperBound']],
            left_on=group_col,
            right_index=True,
            how='left'
        )

        # Step 3: Check outliers
        col_flag = (temp[col] < temp['LowerBound']) | (temp[col] > temp['UpperBound'])

        # Step 4: Combine flags (OR logic)
        outlier_flag = outlier_flag | col_flag

    # Final column
    df['Any_Outlier'] = outlier_flag.map({True:'Yes', False:'No'})

    return df

