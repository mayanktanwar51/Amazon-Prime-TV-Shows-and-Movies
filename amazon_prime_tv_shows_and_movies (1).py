def prepare(df, col, value_col):

    df = df.explode(col)
    df = df[df[col] != ""]

    # reduce clutter
    top = df[col].value_counts().head(10).index
    df = df[df[col].isin(top)]

    agg = (
        df.groupby([col, 'type'])[value_col]
        .agg(
            Mean='mean',
            Q1=lambda x: x.quantile(0.25),
            Q3=lambda x: x.quantile(0.75)
        )
        .reset_index()
    )

    agg['Upper'] = agg['Q3'] + 1.5 * (agg['Q3'] - agg['Q1'])

    agg = agg.melt(
        id_vars=[col, 'type'],
        value_vars=['Mean', 'Q3', 'Upper'],
        var_name='Metric',
        value_name='Score'
    )

    agg['Label'] = agg['type'] + " | " + agg['Metric']

    order = (
        df.groupby(col)[value_col]
        .mean()
        .sort_values(ascending=False)
        .index
    )

    return agg, order


