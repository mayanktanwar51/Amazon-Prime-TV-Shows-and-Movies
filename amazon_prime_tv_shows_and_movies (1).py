def Outliers_deduction_func(df, group_col, target_col):

    IQR_df = df.groupby(group_col)[target_col].agg(
        Q1=lambda x: x.quantile(0.25),
        Q3=lambda x: x.quantile(0.75),
        Min='min',
        Max='max'
    )

    # IQR
    IQR_df['IQR'] = IQR_df['Q3'] - IQR_df['Q1']

    # Bounds
    IQR_df['LowerBound'] = IQR_df['Q1'] - 1.5 * IQR_df['IQR']
    IQR_df['UpperBound'] = IQR_df['Q3'] + 1.5 * IQR_df['IQR']

    # Detect direction
    IQR_df['LeftOutlier'] = IQR_df['Min'] < IQR_df['LowerBound']
    IQR_df['RightOutlier'] = IQR_df['Max'] > IQR_df['UpperBound']

    # Final status
    def get_status(row):
        if row['LeftOutlier'] and row['RightOutlier']:
            return 'Both'
        elif row['LeftOutlier']:
            return 'Left'
        elif row['RightOutlier']:
            return 'Right'
        else:
            return 'No'

    IQR_df['status'] = IQR_df.apply(get_status, axis=1)

    # Convert True/False to Yes/No (optional)
    IQR_df['LeftOutlier'] = IQR_df['LeftOutlier'].map({True: 'Yes', False: 'No'})
    IQR_df['RightOutlier'] = IQR_df['RightOutlier'].map({True: 'Yes', False: 'No'})

    # Reorder columns
    IQR_df = IQR_df[
        ['Q1','Q3','IQR','Min','LowerBound','Max','UpperBound',
         'LeftOutlier','RightOutlier','status']
    ]

    return IQR_df

